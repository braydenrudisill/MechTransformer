import torch
from torch import nn, Tensor
from torch.nn import Transformer
import math


def generate_square_subsequent_mask(size: int, device: str) -> torch.tensor:
    """Creates a tensor with -inf on the upper triangle, and 0 for the diagonal and lower triangle."""

    # TODO: Delete old method
